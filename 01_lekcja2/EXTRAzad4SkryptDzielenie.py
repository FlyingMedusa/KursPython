# Napisz skrypt, który zapyta użytkownika o trzy liczby całkowite,
# a następnie pomnóż dwa pierwsze. przed podzieleniem wyniku przez trzecią liczbę. 
# Wejście i wyjście powinny być zrozumiałe dla użytkownika.
in1 = int(input("Porszę podaj pierwszą liczbę całkowitą:"))
in2 = int(input("Proszę podaj drugą liczbę całkowitą: "))
in3 = int(input("Proszę podaj trzecią liczbę całkowitą: "))
equation = (in1 * in2)/in3
print("Po pomnożeniu dwóch pierwszych liczb, a następnie podzieleniu ich przez trzecią otrzymaliśmy: \n", "\t",equation)